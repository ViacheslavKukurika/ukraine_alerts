
/*-----------------------------------------------------------------------------
  RequestStatus описує життєвий цикл асинхронного запиту до API.

  - initial означає, що запит ще не виконувався;
  - loading — дані зараз завантажуються;
  - success — запит успішно завершився;
  - failure — під час виконання виникла помилка.

  Цей enum не описує наявність або відсутність повітряної тривоги. Він описує
лише стан операції отримання даних. Сам статус тривоги зберігається окремо в
AirRaidStatus.
-----------------------------------------------------------------------------*/

enum RequestStatus {
  initial,
  loading,
  success,
  failure,
}
