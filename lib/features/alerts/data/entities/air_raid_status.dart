/*-----------------------------------------------------------------------------
  AirRaidStatus є внутрішньою типізованою моделлю стану повітряної тривоги, з
 якою працюють Repository, Cubit та UI (наш Entity). 

  Сервер повертає короткі транспортні значення: A, P і N, але інші частини
 застосунку не залежать від циз "сирих" даних. AlertsRepository перетворює їх на
 зрозумілі значення enumів:

  - active — активна повітряна тривога;
  - partial — тривога активна лише в частині регіону;
  - inactive — API достовірно повідомив, що тривоги немає;
  - unknown — достовірний статус ще не отриманий, або значення відповіді не
  вдалося розпізнати. 
-----------------------------------------------------------------------------*/

enum AirRaidStatus {
  active,
  partial,
  inactive,
  unknown,
}
